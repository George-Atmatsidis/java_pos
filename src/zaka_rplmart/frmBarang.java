    private void atur_tabel()
    {
        model.addColumn("Kode Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Harga");
        model.addColumn("Jumlah Stok");
        jTable1.setModel(model);
    }
    
    private void muat_tabel() throws SQLException
    {
        ResultSet rs = configurasi.ambildata("select * from produk");
        while(rs.next())
        {
            model.addRow(new Object[] 
            {
                rs.getString("kode_barang"),
                rs.getString("barang"),
                rs.getString("harga"),
                rs.getString("stok")
            });
        }
    }
    
    private void bersih_tabel()
    {   
        int row = jTable1.getRowCount();
        
        for(int i = 1; i <= row; i++)
        {
           model.removeRow(0);
        }
    }
    
