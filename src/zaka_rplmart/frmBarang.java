    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        try 
        {
            String kode     = txt_kode.getText();
            String barang   = txt_barang.getText();
            String harga    = txt_harga.getText();
            String stok     = txt_stok.getText();
            
            ResultSet rs = configurasi.ambildata("select * from produk where kode_barang = '"+kode+"'");
            if(rs.next())
            {
                JOptionPane.showMessageDialog(this, "Data sudah ada sebelumnya");
            }
            else
            {
                configurasi.aksi("insert into produk set kode_barang = '"+kode+"', barang = '"+barang+"', harga = '"+harga+"', stok = '"+stok+"' ");
            
                //Menghilangkan Isian
                kosong();
            
                JOptionPane.showMessageDialog(this, "Data Berhasil Disimpan"); 
                
                //memuat ulang tabel
                bersih_tabel();
                muat_tabel();
            }       
        }
        catch (Exception e)
        {
            System.out.println("Koneksi Gagal");
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

