    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        try
        {
            //masukkan data ke tabel 
            int no_trans    = Integer.parseInt(txt_no.getText());
            String tanggal  = txt_tgl.getText();
            int total_harga = Integer.parseInt(txt_total.getText());
            configurasi.aksi("insert into transaksi values('"+no_trans+"', '"+tanggal+"', '"+total_harga+"')");
            
            //update stok barang
            int row = jTable1.getRowCount();
            for(int i = 1; i <= row; i ++)
            {
                String kode = jTable1.getValueAt(i-1, 0).toString();
                int jumlah  = Integer.parseInt(jTable1.getValueAt(i-1, 3).toString());
                
                configurasi.aksi("update produk set stok = stok - '"+jumlah+"' where kode_barang = '"+kode+"'");
            }
            
            
            //hapus 
            JOptionPane.showMessageDialog(null, "Data Tersimpan");
            txt_total.setText("");
            txt_kembali.setText("");
            txt_bayar.setText("");
            bersih_tabel();
            buat_nomor();
            
            txt_kode.requestFocus();
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(this, e);
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

