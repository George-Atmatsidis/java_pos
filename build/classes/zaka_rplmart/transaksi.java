    private void txt_jumlahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_jumlahActionPerformed
        // TODO add your handling code here:
        
        int stok_op         = 5;
        int stok            = Integer.parseInt(txt_stok.getText());
        int stok_tersedia   = stok - stok_op;
        int jumlah          = Integer.parseInt(txt_jumlah.getText());
        
        if(stok_tersedia >= jumlah)
        {
            int harga = Integer.parseInt(txt_harga.getText());
            int total = harga * jumlah;
            
            txt_jumlah_harga.setText(Integer.toString(total));
            btn_tambah.requestFocus();
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Maaf stok tidak cukup");
        }
    }//GEN-LAST:event_txt_jumlahActionPerformed

    private void btn_tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahActionPerformed
        // TODO add your handling code here:
        try 
        {
            model.addRow(new Object[] { 
                txt_kode.getText(),
                txt_nama.getText(),
                txt_harga.getText(),
                txt_jumlah.getText(),
                txt_jumlah_harga.getText()
            });
        
            jTable1.setModel(model);
            
            sumTotal += Long.parseLong(txt_jumlah_harga.getText());
            txt_total.setText(Long.toString(sumTotal));
            kosong();
            
            int answer = JOptionPane.showConfirmDialog(this, "Tambah item lagi ? ", "Rpl Message" ,JOptionPane.YES_NO_OPTION);
            if(answer == JOptionPane.YES_OPTION)
            {
                txt_kode.requestFocus();
            }
            else
            {
                txt_bayar.requestFocus();
            }
        }
        catch (Exception e)
        {
            
        }
    }//GEN-LAST:event_btn_tambahActionPerformed

