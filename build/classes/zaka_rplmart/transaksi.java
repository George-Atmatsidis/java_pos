    public void tgl_skrg()
    {
        Date skrg = new Date();
        SimpleDateFormat format = new SimpleDateFormat("dd MMMM yyyy");
        String tanggal = format.format(skrg);
        txt_tgl.setText(tanggal);
    }
    
    public void atur_tabel()
    {
        model.addColumn("Kode Barang");
        model.addColumn("Nama Barang");
        model.addColumn("Harga Barang");
        model.addColumn("Qty Barang");
        model.addColumn("Jumlah Harga");
        jTable1.setModel(model);
    }
    
    public void buat_nomor()
    {
        try
        {
            int no = 1;
            
            ResultSet rs = configurasi.ambildata("select no_trans as auto from transaksi order by no_trans desc");
            if(rs.next())
            {
                no = Integer.parseInt(rs.getString("auto")) + 1;
            }
            
            txt_no.setText(Integer.toString(no));
        }
        catch (Exception e)
        {
            
        }
    }
    
    public void kosong()
    {
        txt_kode.setText("");
        txt_nama.setText("");
        txt_harga.setText("");
        txt_stok.setText("");
        txt_jumlah.setText("");
        txt_jumlah_harga.setText("");
    }
    
    public void bersih_tabel()
    {
        int row = jTable1.getRowCount();
        for(int i = 1; i <= row; i++)
        {
            model.removeRow(0);
        }
    }
        
