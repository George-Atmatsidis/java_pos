    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        try
        {
            int answer = JOptionPane.showConfirmDialog(this, "Apa anda yakin akan menghapus ?" , "Hapus Barang", JOptionPane.YES_NO_OPTION);
            if(answer == JOptionPane.YES_OPTION)
            {
                int row = jTable1.getSelectedRow();
                
                sumTotal -= Long.parseLong(jTable1.getValueAt(row,4).toString());
                txt_total.setText(Long.toString(sumTotal));
                model.removeRow(row);
            }
        }
        catch (Exception e)
        {
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

