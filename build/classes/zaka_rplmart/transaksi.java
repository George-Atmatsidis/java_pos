    private void txt_kodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_kodeActionPerformed
        // TODO add your handling code here:
        try
        {
            ResultSet rs = configurasi.ambildata("select * from produk where kode_barang = '"+txt_kode.getText()+"' ");
            if(rs.next())
            {
                txt_nama.setText(rs.getString("barang"));
                txt_stok.setText(rs.getString("stok"));
                txt_harga.setText(rs.getString("harga"));
                txt_jumlah.requestFocus();
            }
            else
            {
                JOptionPane.showMessageDialog(this, "Maaf, barang belum terdaftar");
                txt_kode.selectAll();
            }
        }
        catch (Exception e)
        {
            
        }
    }//GEN-LAST:event_txt_kodeActionPerformed

