    private void btn_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_simpanActionPerformed
        // TODO add your handling code here:
        int kode        = Integer.parseInt(txt_kode.getText());
        String nama     = txt_nama.getText();
        String user     = txt_user.getText();
        String password = txt_password.getText();
       
        //menentukan jenis kelamin
        String jklm = "";
        if(radio_laki.isSelected())
        {
            jklm = "laki-laki";
        }
        else if(radio_perempuan.isSelected())
        {
            jklm = "perempuan";
        }
        
        //menentukan level
        String level = "";
        if(radio_admin.isSelected())
        {
            level = "admin";
        }
        else if(radio_kasir.isSelected())
        {
            level = "kasir";
        }
        
        try
        {
            ResultSet rs = configurasi.ambildata("select * from petugas where kode_petugas = '"+kode+"'");
            if(rs.next())
            {
                txt_nama.setText(rs.getString("nama"));
                txt_user.setText(rs.getString("user"));
                txt_password.setText(rs.getString("password"));
                
                //menentukan jenis kelamin
                if(rs.getString("jklm").equals("laki-laki"))
                {
                    radio_laki.setSelected(true);
                }
                else
                {
                    radio_perempuan.setSelected(true);
                }
                
                //menentukan level
                if(rs.getString("level").equals("admin"))
                {
                    radio_admin.setSelected(true);
                }
                else
                {
                    radio_kasir.setSelected(true);
                }
                
                JOptionPane.showMessageDialog(this, "Data sudah ada");
            }
            else
            {
                configurasi.aksi("insert into petugas values('"+kode+"', '"+nama+"', '"+jklm+"', '"+user+"', '"+password+"', '"+level+"')");
                bersih();
                txt_kode.requestFocus();
                bersih_tabel();
                muat_tabel();
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan");
            }
        }
        catch (Exception e)
        {
            
        }
    }//GEN-LAST:event_btn_simpanActionPerformed

