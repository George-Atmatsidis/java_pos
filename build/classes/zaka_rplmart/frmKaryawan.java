    public void muat_tabel()
    {
        try
        {
            ResultSet rs = configurasi.ambildata("select * from petugas");
            while(rs.next())
            {
                model.addRow(new Object[] {
                    rs.getString("kode_petugas"),
                    rs.getString("nama"),
                    rs.getString("jklm"),
                    rs.getString("user"),
                    rs.getString("password"),
                    rs.getString("level")
                });
            }
        }
        catch (Exception e)
        {
            
        }
    }
    
    public void bersih_tabel()
    {
        int row = jTable1.getRowCount();
        for(int i = 1; i <= row; i++)
        {
            model.removeRow(0);
        }
    }

